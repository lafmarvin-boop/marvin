package com.pixelhero.app

/**
 * Tiny 5x7 bitmap font for stamping uppercase text + digits + basic punctuation
 * onto pixel art. Suitable for game UI mockups (HP bars, score, names).
 */
object PixelFont {
    // Each glyph is a 5-wide × 7-tall pattern: '#' = pixel ON, '.' = OFF
    private val GLYPHS: Map<Char, Array<String>> = mapOf(
        'A' to arrayOf(".###.","#...#","#...#","#####","#...#","#...#","#...#"),
        'B' to arrayOf("####.","#...#","#...#","####.","#...#","#...#","####."),
        'C' to arrayOf(".###.","#...#","#....","#....","#....","#...#",".###."),
        'D' to arrayOf("####.","#...#","#...#","#...#","#...#","#...#","####."),
        'E' to arrayOf("#####","#....","#....","####.","#....","#....","#####"),
        'F' to arrayOf("#####","#....","#....","####.","#....","#....","#...."),
        'G' to arrayOf(".###.","#...#","#....","#..##","#...#","#...#",".###."),
        'H' to arrayOf("#...#","#...#","#...#","#####","#...#","#...#","#...#"),
        'I' to arrayOf(".###.","..#..","..#..","..#..","..#..","..#..",".###."),
        'J' to arrayOf("..###","...#.","...#.","...#.","...#.","#..#.",".##.."),
        'K' to arrayOf("#...#","#..#.","#.#..","##...","#.#..","#..#.","#...#"),
        'L' to arrayOf("#....","#....","#....","#....","#....","#....","#####"),
        'M' to arrayOf("#...#","##.##","#.#.#","#.#.#","#...#","#...#","#...#"),
        'N' to arrayOf("#...#","##..#","#.#.#","#..##","#...#","#...#","#...#"),
        'O' to arrayOf(".###.","#...#","#...#","#...#","#...#","#...#",".###."),
        'P' to arrayOf("####.","#...#","#...#","####.","#....","#....","#...."),
        'Q' to arrayOf(".###.","#...#","#...#","#...#","#.#.#","#..#.",".##.#"),
        'R' to arrayOf("####.","#...#","#...#","####.","#.#..","#..#.","#...#"),
        'S' to arrayOf(".####","#....","#....",".###.","....#","....#","####."),
        'T' to arrayOf("#####","..#..","..#..","..#..","..#..","..#..","..#.."),
        'U' to arrayOf("#...#","#...#","#...#","#...#","#...#","#...#",".###."),
        'V' to arrayOf("#...#","#...#","#...#","#...#","#...#",".#.#.","..#.."),
        'W' to arrayOf("#...#","#...#","#...#","#.#.#","#.#.#","##.##","#...#"),
        'X' to arrayOf("#...#","#...#",".#.#.","..#..",".#.#.","#...#","#...#"),
        'Y' to arrayOf("#...#","#...#",".#.#.","..#..","..#..","..#..","..#.."),
        'Z' to arrayOf("#####","....#","...#.","..#..",".#...","#....","#####"),
        '0' to arrayOf(".###.","#...#","#..##","#.#.#","##..#","#...#",".###."),
        '1' to arrayOf("..#..",".##..","..#..","..#..","..#..","..#..",".###."),
        '2' to arrayOf(".###.","#...#","....#","...#.","..#..",".#...","#####"),
        '3' to arrayOf("####.","....#","....#",".###.","....#","....#","####."),
        '4' to arrayOf("...#.","..##.",".#.#.","#..#.","#####","...#.","...#."),
        '5' to arrayOf("#####","#....","####.","....#","....#","#...#",".###."),
        '6' to arrayOf(".###.","#....","#....","####.","#...#","#...#",".###."),
        '7' to arrayOf("#####","....#","...#.","..#..",".#...","#....","#...."),
        '8' to arrayOf(".###.","#...#","#...#",".###.","#...#","#...#",".###."),
        '9' to arrayOf(".###.","#...#","#...#",".####","....#","....#",".###."),
        ' ' to arrayOf(".....",".....",".....",".....",".....",".....","....."),
        '.' to arrayOf(".....",".....",".....",".....",".....","..#..","..#.."),
        ',' to arrayOf(".....",".....",".....",".....","..#..","..#..",".#..."),
        '!' to arrayOf("..#..","..#..","..#..","..#..","..#..",".....","..#.."),
        '?' to arrayOf(".###.","#...#","....#","...#.","..#..",".....","..#.."),
        ':' to arrayOf(".....","..#..",".....",".....",".....","..#..","....."),
        '-' to arrayOf(".....",".....",".....",".###.",".....",".....","....."),
        '+' to arrayOf(".....","..#..","..#..","#####","..#..","..#..","....."),
        '#' to arrayOf(".#.#.",".#.#.","#####",".#.#.","#####",".#.#.",".#.#."),
        '%' to arrayOf("##..#","##.#.","..#..",".#...","#.##.","#.##.","....#"),
        '/' to arrayOf("....#","...#.","..#..","..#..",".#...","#....","#...."),
        '(' to arrayOf("..##.",".#...","#....","#....","#....",".#...","..##."),
        ')' to arrayOf(".##..","...#.","....#","....#","....#","...#.",".##..")
    )

    private val GLYPH_HEIGHT = 7
    private val GLYPH_WIDTH = 5

    /** Render [text] (case-insensitive, uppercased) onto [frame] at top-left (x, y) with [color]. */
    fun render(frame: Frame, x: Int, y: Int, text: String, color: Int, charSpacing: Int = 1) {
        var cx = x
        for (raw in text.uppercase()) {
            val glyph = GLYPHS[raw] ?: GLYPHS[' ']!!
            for (gy in 0 until GLYPH_HEIGHT) {
                val row = glyph[gy]
                for (gx in 0 until GLYPH_WIDTH) {
                    if (gx < row.length && row[gx] == '#') {
                        val px = cx + gx; val py = y + gy
                        if (px in 0 until frame.width && py in 0 until frame.height) {
                            frame.set(px, py, color)
                        }
                    }
                }
            }
            cx += GLYPH_WIDTH + charSpacing
        }
    }

    /** Compute the bounding width of [text] when rendered (in pixels). */
    fun textWidth(text: String, charSpacing: Int = 1): Int =
        if (text.isEmpty()) 0 else text.length * (GLYPH_WIDTH + charSpacing) - charSpacing

    val height: Int get() = GLYPH_HEIGHT
}
